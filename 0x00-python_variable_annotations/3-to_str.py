#!/usr/bin/env python3
"""
This module defines a type-annotated function
for converting a float to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a float number to a string.

    Args:
        n (float): The float number.

    Returns:
        str: The string representation of the float number.
    """
    return str(n)
